package Basicsofjava;

public class updatingvariables {
    public static void main(String[] args) {
        int x = 8;
        System.out.println(x);
        x = 9;
        System.out.println(x);
        x = x + 7;
        System.out.println(x);
        x = x - 20;
        System.out.println(x);
    }
}
