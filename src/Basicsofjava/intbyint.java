package Basicsofjava;

public class intbyint {
    public static void main(String[] args) {
        System.out.println(5/2); // int/int will give u integer value.
        System.out.println(5.0/2.0); // and if there is double then it will give double value.
        System.out.println(5.0/2);
        System.out.println(5/2.0);
    }
}
